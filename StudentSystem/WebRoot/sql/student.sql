
create table student(
id int primary key  auto_increment,
studet_no varchar(30),
studet_name varchar(30),
studet_sex varchar(2),
department varchar(30),
grade varchar(30),
professional varchar(30),
phonenumber varchar(30),
int_time varchar(30),
out_time varchar(30),
building_no varchar(30),
dormitory_no varchar(30)
);